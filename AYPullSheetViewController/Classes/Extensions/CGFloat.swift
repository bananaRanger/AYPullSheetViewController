import UIKit

//MARK: - CGFloat extension
extension CGFloat {
  public static func percent(_ percent: Int, from value: CGFloat) -> CGFloat {
    guard percent >= 0 || percent <= 100 else { return value }
    let percent = CGFloat(percent)
    return value * (percent / 100)
  }
}
